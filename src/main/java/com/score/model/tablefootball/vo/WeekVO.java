package com.score.model.tablefootball.vo;

public class WeekVO {
	private int points = 0;
	private int games = 0;

	public WeekVO() {
		super();
	}

	public int getPoints() {
		return points;
	}

	public void setPoints(int points) {
		this.points = points;
	}

	public int getGames() {
		return games;
	}

	public void setGames(int games) {
		this.games = games;
	}

}
